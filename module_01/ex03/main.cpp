#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

static void	attackWithHumanA( void ) {
	Weapon club = Weapon("crude spiked club");
	HumanA bob("Bob", club);
	bob.attack();
	club.setType("some other type of club");
	bob.attack();
}

static void	attackWithHumanB( void ) {
	Weapon club = Weapon("crude spiked club");
	HumanB jim("Jim");
	jim.attack();
	jim.setWeapon(club);
	jim.attack();
	club.setType("some other type of club");
	jim.attack();
}

int		main( void ) {
	attackWithHumanA();
	attackWithHumanB();
	return 0;
}
